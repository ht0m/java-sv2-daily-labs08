package day05;

public class ValuesOfLines {
    private String col1;
    private int value;

    public ValuesOfLines(String col1, int value) {
        this.col1 = col1;
        this.value = value;
    }

    public String getCol1() {
        return col1;
    }

    public int getValue() {
        return value;
    }
}
